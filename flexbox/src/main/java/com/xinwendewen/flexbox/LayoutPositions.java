package com.xinwendewen.flexbox;

public class LayoutPositions {
    final int left;
    final int top;
    final int right;
    final int bottom;

    LayoutPositions(int left, int top, int right, int bottom) {
        this.left = left;
        this.top = top;
        this.right = right;
        this.bottom = bottom;
    }
}
